package learn01.basic01;

public class LoopExample {
    public static void main(String[] args) {
        // for문
        for (int i = 0; i < 5; i++) {
            System.out.println("for 반복: " + i);
        }

        // while문
        int j = 0;
        while (j < 5) {
            System.out.println("while 반복: " + j);
            j++;
        }
    }
}
